const MORSE_TABLE = {
    '.-': 'a',
    '-...': 'b',
    '-.-.': 'c',
    '-..': 'd',
    '.': 'e',
    '..-.': 'f',
    '--.': 'g',
    '....': 'h',
    '..': 'i',
    '.---': 'j',
    '-.-': 'k',
    '.-..': 'l',
    '--': 'm',
    '-.': 'n',
    '---': 'o',
    '.--.': 'p',
    '--.-': 'q',
    '.-.': 'r',
    '...': 's',
    '-': 't',
    '..-': 'u',
    '...-': 'v',
    '.--': 'w',
    '-..-': 'x',
    '-.--': 'y',
    '--..': 'z',
    '.----': '1',
    '..---': '2',
    '...--': '3',
    '....-': '4',
    '.....': '5',
    '-....': '6',
    '--...': '7',
    '---..': '8',
    '----.': '9',
    '-----': '0',
};

function decode(expr) {
    // write your solution here
    let len = 10;
    let arr = [];
    var regex = new RegExp('.{' + len + '}|.{1,' + Number(len - 1) + '}', 'g');
    expr.match(regex).map(function(num) {
        return num.replace(/^0+/, '').replace(/11/g, "-").replace(/10/g, ".");
    }).forEach((item) => item in MORSE_TABLE ? arr.push(MORSE_TABLE[item]) : arr.push(' '));

    return arr.join('');
}

module.exports = {
    decode
}